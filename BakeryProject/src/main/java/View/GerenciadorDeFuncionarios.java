    public void preencherCamposEdicao(int rowIndex) {
        DefaultTableModel model = (DefaultTableModel) tabelaFuncionarios.getModel();
        funcionarioEditandoId = (Long) model.getValueAt(rowIndex, 0);

        nomeFuncionarioEdit.setText((String) model.getValueAt(rowIndex, 1));
        cpfFuncionarioEdit.setText((String) model.getValueAt(rowIndex, 2));

        String dataStr = (String) model.getValueAt(rowIndex, 3);
        if (dataStr != null && !dataStr.isEmpty()) {
            try {
                LocalDate data = LocalDate.parse(dataStr);
                DateTimeFormatter formatoBrasileiro = DateTimeFormatter.ofPattern("dd/MM/yyyy");
                jDataTextEdit.setText(data.format(formatoBrasileiro));
            } catch (DateTimeParseException e) {
                jDataTextEdit.setText(dataStr);
            }
        }

        enderecoFuncionarioEdit.setText((String) model.getValueAt(rowIndex, 4));
        telefoneFuncionarioEdit.setText((String) model.getValueAt(rowIndex, 5));
        emailFuncionarioEdit.setText((String) model.getValueAt(rowIndex, 6));
        cargoFuncionarioEdit.setText((String) model.getValueAt(rowIndex, 7));
        departamentoFuncionarioEdit.setText((String) model.getValueAt(rowIndex, 8));
        salarioFuncionarioEdit.setText(model.getValueAt(rowIndex, 9).toString());
        jTabbedPane1.setSelectedIndex(1);
    }
    
    public void editarFuncionario() {
        if (funcionarioEditandoId == null) {
            JOptionPane.showMessageDialog(this, "Nenhum funcionário selecionado para edição", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            LocalDate dataNascimento;
            try {
                DateTimeFormatter formatoBrasileiro = DateTimeFormatter.ofPattern("dd/MM/yyyy");
                dataNascimento = LocalDate.parse(jDataTextEdit.getText(), formatoBrasileiro);
            } catch (DateTimeParseException e1) {
                try {
                    dataNascimento = LocalDate.parse(jDataTextEdit.getText());
                } catch (DateTimeParseException e2) {
                    throw new IllegalArgumentException("Formato de data inválido! Use dd/MM/aaaa ou aaaa-MM-dd");
                }
            }

            Funcionario funcionario = new Funcionario(
                funcionarioEditandoId,
                nomeFuncionarioEdit.getText(),
                cpfFuncionarioEdit.getText(),
                dataNascimento,
                enderecoFuncionarioEdit.getText(),
                telefoneFuncionarioEdit.getText(),
                emailFuncionarioEdit.getText(),
                cargoFuncionarioEdit.getText(),
                departamentoFuncionarioEdit.getText(),
                Double.parseDouble(salarioFuncionarioEdit.getText())
            );

            FuncionarioDAO dao = new FuncionarioDAO();
            dao.atualizar(funcionario);

            JOptionPane.showMessageDialog(this, "Funcionário atualizado com sucesso!");
            limparCamposEdicao();
            popularFuncionarios();
        } catch (DateTimeParseException e) {
            JOptionPane.showMessageDialog(this, "Formato de data inválido! Use o formato dd/MM/aaaa", "Erro", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Salário deve ser um número válido!", "Erro", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao atualizar: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }
    
