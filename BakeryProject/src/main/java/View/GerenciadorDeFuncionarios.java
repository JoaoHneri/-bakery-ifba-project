    public void mostrarPopupMenu(MouseEvent e) {
        int row = tabelaFuncionarios.rowAtPoint(e.getPoint());
        if (row >= 0) {
            tabelaFuncionarios.setRowSelectionInterval(row, row);

            JPopupMenu popupMenu = new JPopupMenu();
            
            JMenuItem verDetalhes = new JMenuItem("Ver Detalhes");
            verDetalhes.addActionListener(ev -> abrirDetalhesFuncionario(row));
            popupMenu.add(verDetalhes);
            
            
            JMenuItem deletarItem = new JMenuItem("Deletar");
            deletarItem.addActionListener(ev -> confirmarDelecao(row));
            popupMenu.add(deletarItem);
            
            JMenuItem editarItem = new JMenuItem("Editar Funcionário");
            editarItem.addActionListener(ev -> preencherCamposEdicao(row));
            popupMenu.add(editarItem);
            
            popupMenu.show(e.getComponent(), e.getX(), e.getY());
        }
    }
    
